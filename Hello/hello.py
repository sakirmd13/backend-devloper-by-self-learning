#introduction to python

#-> Python is a high-level, interpreted programming language known for its clear syntax, readability, and versatility across industries. Created by Guido van Rossum and released in 1991, it has become a dominant language for machine learning, data science, web development, and automation


#Why use python

#->Readable Syntax: Code looks similar to plain English sentences.
# Interpreted Language: Code executes line-by-line without prior compilation.
# Dynamically Typed: Variables do not need explicit type declarations.
# Multi-paradigm: Supports procedural, object-oriented, and functional programming.
# Batteries Included: Features a massive standard library for diverse tasks



